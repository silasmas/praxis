                <h3> Quand est-ce que cela à commencer ?</h3>
                <p>
                    À la fin de mes études secondaires, j'ai pris conscience que je devais faire face à ma vie d'adulte.
                    Je savais que je devais travailler et subvenir à mes besoins. Au fond de moi, je ressentais un désir
                    ardent d'entreprendre, d'être autonome et indépendant. J'ai alors lancé mon premier business en
                    vendant du pétrole pour l'éclairage domestique, appelé « Kingongolo », en 2006 avec seulement 5
                    litres de marchandise de base. En l'espace d'un an, j'avais déjà 30 fûts de pétrole. J'ai embauché
                    d'autres vendeurs et j'ai maintenu la discipline personnelle en évoluant toujours en tant que
                    vendeur ambulant de pétrole.
                </p>
                <p>
                    En 2008, j'ai été frappé par la maladie, ce qui a entraîné la chute de mon business et sa
                    disparition. J'ai alors décidé de retourner à Kimpese, dans la maison familiale près de mes parents.
                    Entre-temps, ma femme attendait notre premier enfant. En 2010, ma mère m'a encouragé à réfléchir
                    sérieusement à mes responsabilités. Elle m'a remis 15 $ pour que je m'inscrive à l'auto-école et
                    cherche un emploi en tant que chauffeur.
                </p>
                <p>
                    Cependant, l'esprit d'entrepreneur ne m'a jamais quitté. J'ai utilisé cet argent pour démarrer un
                    nouveau projet en devenant vendeur ambulant de boudin. J'ai lancé cette activité alors que je
                    devenais déjà père de famille.
                    Face à la réalité, en 2012, j'ai été employé dans une entreprise appartenant à des Indiens où je
                    gagnais 140 000 CDF (cent quarante mille Francs). Au bout d'un an, j'ai démissionné de ce travail,
                    malgré l'opposition de ma mère, pour retourner à ma passion première : l'entrepreneuriat.

                </p>
                <p>
                    Je suis allé à Lufu où j'ai commencé un business de vente de « makayabu » (poisson salé) avec
                    l'encouragement de "Tonton Diabondo", un jeune du quartier qui était enthousiaste à l'idée de
                    travailler au port de Lufu comme porteur.

                    Un jour, alors que j'étais à Lufu, « Jancy Zola » un ancien camarade de classe revenu de l'Angola,
                    cherchait un déclarant pour dédouaner ses six conteneurs. J'ai pris mon alors pour m’improvisé en
                    tant que déclarant pour lui, malgré mon manque d'expérience dans ce domaine.

                    Je me suis alors souvenu que mon beau-père était déclarant. Je l'ai appelé et il a accepté de
                    m'accompagner à distance, car il ne pouvait pas quitter ses fonctions à Matadi, où il résidait.
                    Finalement, tous s’est bien passé, tous les documents de douane ont été obtenus, pour la grande
                    satisfaction de mon ami. C'était là mon tout premier contrat qui a marqué le début de mon parcours
                    entrepreneurial.

                    Au retour de Jancy, il m'a recommandé à tout son réseau, y compris Trésor Mengi qui par la suite a
                    été un partenaire important à mon business. C'est à partir de là que GLODEMAP a vu le jour.

                </p>
